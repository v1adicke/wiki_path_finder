def _pick_pairs(pool: List[Tuple[str, str]], count: int, rng: random.Random) -> List[Tuple[str, str]]:
    if count <= len(pool):
        return rng.sample(pool, k=count)
    return [rng.choice(pool) for _ in range(count)]


def generate_cases(total_cases: int, seed: int = 42) -> List[BenchmarkCase]:
    total_cases = max(30, total_cases)

    rng = random.Random(seed)

    easy_count = int(total_cases * 0.15)
    medium_count = int(total_cases * 0.25)
    hard_count = int(total_cases * 0.35)
    very_hard_count = total_cases - easy_count - medium_count - hard_count

    cases: List[BenchmarkCase] = []

    for idx, (start, end) in enumerate(_pick_pairs(EASY_PAIRS, easy_count, rng), start=1):
        cases.append(BenchmarkCase(case_id=f"E-{idx:04d}", start=start, end=end, difficulty="easy"))

    for idx, (start, end) in enumerate(_pick_pairs(MEDIUM_PAIRS, medium_count, rng), start=1):
        cases.append(BenchmarkCase(case_id=f"M-{idx:04d}", start=start, end=end, difficulty="medium"))

    for idx, (start, end) in enumerate(_pick_pairs(HARD_PAIRS, hard_count, rng), start=1):
        cases.append(BenchmarkCase(case_id=f"H-{idx:04d}", start=start, end=end, difficulty="hard"))

    for idx, (start, end) in enumerate(_pick_pairs(VERY_HARD_PAIRS, very_hard_count, rng), start=1):
        cases.append(BenchmarkCase(case_id=f"V-{idx:04d}", start=start, end=end, difficulty="very_hard"))

    rng.shuffle(cases)
    return cases
